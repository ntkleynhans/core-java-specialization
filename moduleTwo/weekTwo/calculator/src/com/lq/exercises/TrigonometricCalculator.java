package com.lq.exercises;

import java.lang.Math;

public class TrigonometricCalculator {

    public final static double sine(double x) {
        return Math.sin(x);
    }

    public final static double cosine(double x) {
        return Math.cos(x);
    }

    public final static double tangent(double x) {
        return Math.tan(x);
    }

    public final static double arcsine(double x) {
        return Math.asin(x);
    }

    public final static double arccosine(double x) {
        return Math.acos(x);
    }

    public final static double arctangent(double x) {
        return Math.atan(x);
    }
}
