package com.lq.exercises;

public class BasicCalculator {

    public final static double add(double x, double y) {
        return x + y;
    }

    public final static double subtract(double x, double y) {
        return x - y;
    }

    public final static double multiply(double x, double y) {
        return x * y;
    }

    public final static double divide(double x, double y) {
        return x / y;
    }

}
